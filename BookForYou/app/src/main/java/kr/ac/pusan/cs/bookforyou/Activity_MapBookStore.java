        ((TextView) view.findViewById(R.id.description)).setText(map.get(marker.getId()).usedNum +" / " + map.get(marker.getId()).maxNum);
        ((TextView) view.findViewById(R.id.brief)).setText(marker.getSnippet());
        //((TextView) view.findViewById(R.id.description)).setText(R.string.middle_lorem_ipsum);
