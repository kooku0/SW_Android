    //여기부터는 GPS 활성화를 위한 메소드들
    private void showDialogForLocationServiceSetting() {

        AlertDialog.Builder builder = new AlertDialog.Builder(Activity_MapBookStore.this);
        builder.setTitle("위치 서비스 비활성화");
        builder.setMessage("앱을 사용하기 위해서는 위치 서비스가 필요합니다.\n"
                + "위치 설정을 수정하실래요?");
        builder.setCancelable(true);
        builder.setPositiveButton("설정", new DialogInterface.OnClickListener() {
            @Override
            public void onClick(DialogInterface dialog, int id) {
                Intent callGPSSettingIntent
                        = new Intent(android.provider.Settings.ACTION_LOCATION_SOURCE_SETTINGS);
                startActivityForResult(callGPSSettingIntent, GPS_ENABLE_REQUEST_CODE);
            }
        });
        builder.setNegativeButton("취소", new DialogInterface.OnClickListener() {
            @Override
            public void onClick(DialogInterface dialog, int id) {
                dialog.cancel();
            }
        });
        builder.create().show();
    }


    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        switch (requestCode) {

            case GPS_ENABLE_REQUEST_CODE:

                //사용자가 GPS 활성 시켰는지 검사
                if (checkLocationServicesStatus()) {
                    if (checkLocationServicesStatus()) {

                        Log.d(TAG, "onActivityResult : 퍼미션 가지고 있음");


                        if ( mGoogleApiClient.isConnected() == false ) {

                            Log.d( TAG, "onActivityResult : mGoogleApiClient connect ");
                            mGoogleApiClient.connect();
                        }
                        return;
                    }
                }

                break;
        }
    }
    public void AddMarker(){
        mRef.child("Book_Store").addListenerForSingleValueEvent(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {

                map.clear();
                for(DataSnapshot snapshot : dataSnapshot.getChildren()){

                    Obj_bookStore bookStore = new Obj_bookStore();
                    bookStore = snapshot.getValue(Obj_bookStore.class);


                    MarkerOptions markerOptions = new MarkerOptions();
                    Location location = new Location("servide Provider");
                    location.setLatitude(bookStore.location.x);
                    location.setLongitude(bookStore.location.y);

                    markerOptions.position(new LatLng(bookStore.location.x,bookStore.location.y));
                    String markerTitle = getCurrentAddress(new LatLng( location.getLatitude(), location.getLongitude()));
                    String markerSnippet = "위도:" + String.valueOf(location.getLatitude())
                            + " 경도:" + String.valueOf(location.getLongitude());
                    markerOptions.title(markerTitle);
                    markerOptions.snippet(markerSnippet);
                    if(bookStore.maxNum==bookStore.usedNum);
                    else if(bookStore.usedNum==0)markerOptions.icon(BitmapDescriptorFactory.defaultMarker(200f));
                    else markerOptions.icon(BitmapDescriptorFactory.defaultMarker(100f));
                    Marker mMarker = mGoogleMap.addMarker(markerOptions);
                    map.put(mMarker.getId(),bookStore);
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {

            }
        });


    }

